const { expect } = require(`chai`);
const binDecode = require("./solution");


describe("Binary Decoding Test", () => {
    it("0100011011010111100100100000011011100110000101101101011001010010000001101001011100110010 to be 'my name is'", () => {
        expect(binDecode('0100011011010111100100100000011011100110000101101101011001010010000001101001011100110010'))
        .to.be.equal("my name is")
    })
    it("010011100110110111101100110011101000111010101101110011010010010000001110011011101000111010101100100011001010110111001110100010 to be 'softuni student'", () => {
        expect(binDecode('010011100110110111101100110011101000111010101101110011010010010000001110011011101000111010101100100011001010110111001110100010'))
        .to.be.equal("softuni student")
    })
})